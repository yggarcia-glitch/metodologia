"use strcit";
// solicitamos ingresar dos numeros y realizar las 4 operaciones basicas

let a = Number(prompt ("ingresar valor a"));
let b = Number(prompt ("ingresar b"));

let suma = a+b;
//"el resultado de la suma: " + a --> concatenar
alert ("el resutado de la suma es: " +  suma);

let c = Number(prompt ("ingresar valor c"));
let d = Number(prompt ("ingresar d"));
let resta = c-d;
alert ("el resutado de la resta es: " +  resta);

let e = Number(prompt ("ingresar valor e"));
let f = Number(prompt ("ingresar f"));
let division = e/f;
alert ("el resutado de la division es: " +  division);

let g = Number(prompt ("ingresar valor g"));
let h = Number(prompt ("ingresar h"));
